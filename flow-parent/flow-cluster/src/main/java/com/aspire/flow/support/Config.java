package com.aspire.flow.support;

import java.util.Properties;

public class Config {
	private String port;
	private String zookeeperAddresses;
	private String nodeModel;
	private Properties properties;
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getZookeeperAddresses() {
		return zookeeperAddresses;
	}
	public void setZookeeperAddresses(String zookeeperAddresses) {
		this.zookeeperAddresses = zookeeperAddresses;
	}
	public String getNodeModel() {
		return nodeModel;
	}
	public void setNodeModel(String nodeModel) {
		this.nodeModel = nodeModel;
	}
	public Properties getProperties() {
		return properties;
	}
	public void setProperties(Properties properties) {
		this.properties = properties;
	}
	
}
