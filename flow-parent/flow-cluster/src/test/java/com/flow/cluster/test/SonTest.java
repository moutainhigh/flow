package com.flow.cluster.test;

public class SonTest implements Comparable<SonTest>{
	private int age;
	private String name;
	
	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int compareTo(SonTest o) {
		
		return this.age - o.getAge();
	}

}
