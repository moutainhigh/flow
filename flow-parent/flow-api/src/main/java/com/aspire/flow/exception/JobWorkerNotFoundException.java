package com.aspire.flow.exception;

/**
 * 当没有 找到 JobWorker 节点的时候抛出这个异常
 * @author chenhaitao
 *
 */
public class JobWorkerNotFoundException extends Exception{

	private static final long serialVersionUID = -7804693020495753429L;

	public JobWorkerNotFoundException() {
    }

    public JobWorkerNotFoundException(String message) {
        super(message);
    }

    public JobWorkerNotFoundException(String message, Throwable cause) {
        super(message, cause);
    }

    public JobWorkerNotFoundException(Throwable cause) {
        super(cause);
    }

}
