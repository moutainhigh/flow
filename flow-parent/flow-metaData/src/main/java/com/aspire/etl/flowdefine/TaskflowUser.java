package com.aspire.etl.flowdefine;

public class TaskflowUser {
	private Integer taskflowID;
	
	private String userID;

	public TaskflowUser(Integer taskflowID, String userID) {
		super();
		this.taskflowID = taskflowID;
		this.userID = userID;
	}

	public TaskflowUser() {
	}

	public Integer getTaskflowID() {
		return taskflowID;
	}

	public void setTaskflowID(Integer taskflowID) {
		this.taskflowID = taskflowID;
	}

	public String getUserID() {
		return userID;
	}

	public void setUserID(String userID) {
		this.userID = userID;
	}

}
